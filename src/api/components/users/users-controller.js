const usersService = require('./users-service');
const { errorResponder, errorTypes } = require('../../../core/errors');

/**
 * Handle get list of users request
 * @param {object} request - Express request object
 * @param {object} response - Express response object
 * @param {object} next - Express route middlewares
 * @returns {object} Response object or pass an error to the next route
 */
async function getUsers(request, response, next) {
  try {
    // Parse query parameters
    const page = parseInt(request.query.page_number) || 1;
    const pageSize = parseInt(request.query.page_size) || 10;
    const sort = parseSortParam(request.query.sort);
    const search = parseSearchParam(request.query.search);

    // Get users with pagination and filtering
    const users = await usersService.getUsers(page, pageSize, sort, search);

    // Return paginated and filtered users
    return response.status(200).json(users);
  } catch (error) {
    return next(error);
  }
}

/**
 * Parse sort parameter from query string
 * @param {string} sortParam - Sort parameter from query string
 * @returns {object} Sort object
 */
function parseSortParam(sortParam) {
  if (!sortParam || typeof sortParam !== 'string') {
    return { email: 1 }; // Default sorting by email ascending
  }

  const [field, order] = sortParam.split(':');

  if (!['email', 'name'].includes(field)) {
    return { email: 1 }; // Default sorting by email ascending
  }

  return { [field]: order === 'desc' ? -1 : 1 };
}

/**
 * Parse search parameter from query string
 * @param {string} searchParam - Search parameter from query string
 * @returns {object} Search object
 */
function parseSearchParam(searchParam) {
  if (!searchParam || typeof searchParam !== 'string') {
    return {}; // No search filter applied
  }

  const [field, key] = searchParam.split(':');

  if (!['email', 'name'].includes(field) || !key) {
    return {}; // Invalid search parameter
  }

  return { [field]: { $regex: key, $options: 'i' } }; // Case-insensitive regex search
}

/**
 * Handle get user detail request
 * @param {object} request - Express request object
 * @param {object} response - Express response object
 * @param {object} next - Express route middlewares
 * @returns {object} Response object or pass an error to the next route
 */
async function getUser(request, response, next) {
  try {
    const user = await usersService.getUser(request.params.id);

    if (!user) {
      throw errorResponder(errorTypes.UNPROCESSABLE_ENTITY, 'Unknown user');
    }

    return response.status(200).json(user);
  } catch (error) {
    return next(error);
  }
}

/**
 * Handle create user request
 * @param {object} request - Express request object
 * @param {object} response - Express response object
 * @param {object} next - Express route middlewares
 * @returns {object} Response object or pass an error to the next route
 */
async function createUser(request, response, next) {
  try {
    const name = request.body.name;
    const email = request.body.email;
    const password = request.body.password;
    const password_confirm = request.body.password_confirm;

    // Check confirmation password
    if (password !== password_confirm) {
      throw errorResponder(
        errorTypes.INVALID_PASSWORD,
        'Password confirmation mismatched'
      );
    }

    // Email must be unique
    const emailIsRegistered = await usersService.emailIsRegistered(email);
    if (emailIsRegistered) {
      throw errorResponder(
        errorTypes.EMAIL_ALREADY_TAKEN,
        'Email is already registered'
      );
    }

    const success = await usersService.createUser(name, email, password);
    if (!success) {
      throw errorResponder(
        errorTypes.UNPROCESSABLE_ENTITY,
        'Failed to create user'
      );
    }

    return response.status(200).json({ name, email });
  } catch (error) {
    return next(error);
  }
}

/**
 * Handle update user request
 * @param {object} request - Express request object
 * @param {object} response - Express response object
 * @param {object} next - Express route middlewares
 * @returns {object} Response object or pass an error to the next route
 */
async function updateUser(request, response, next) {
  try {
    const id = request.params.id;
    const name = request.body.name;
    const email = request.body.email;

    // Email must be unique
    const emailIsRegistered = await usersService.emailIsRegistered(email);
    if (emailIsRegistered) {
      throw errorResponder(
        errorTypes.EMAIL_ALREADY_TAKEN,
        'Email is already registered'
      );
    }

    const success = await usersService.updateUser(id, name, email);
    if (!success) {
      throw errorResponder(
        errorTypes.UNPROCESSABLE_ENTITY,
        'Failed to update user'
      );
    }

    return response.status(200).json({ id });
  } catch (error) {
    return next(error);
  }
}

